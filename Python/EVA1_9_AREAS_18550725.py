print("Ingresa el valor de la base mayor (B)")
rBmayor = float(input())
print("Ingresa el valor de la base menor (b)")
rBmenor = float(input())
print("Ingresa el valor de la altura (h)")
rAltura = float(input())
rArea = (rBmayor + rBmenor) * rAltura / 2
print("El área de tu trapecio es: ", end='', flush=True)
print(rArea, end='', flush=True)
